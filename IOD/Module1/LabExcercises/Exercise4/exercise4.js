// function 1 - Subtraction //
function Subtract(a, b) {
  return a - b;
}

// function 2 - Addition //
function Add(a, b) {
  return a + b;
}

// function 3 - Division //
function Divide(a, b) {
  return a / b;
}

// function 4 - Multiplication //
function Multiply(a, b) {
  return a * b;
}

